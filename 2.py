#string manipulations
name = " mythri loves python "
print(name.upper()) #uppercase
print(name.lower()) #lowercase
print(name * 5) # repeating multiple times
print(name.replace("mythri","mahima")) # to replace the string elements
print(name[::2]) #split method
print(name[::])# to show full string
print(name[:-1])# it shows ulta
print("mythri \t loves python")
print("mythri \n loves python")